class Calculadora

	def soma (a,b)
		res = a+b
		puts "O resultado é #{res}"
		return res
	end

	def multiplicacao (a,b)
		res = a*b
		puts "O resultado é #{res}"
		return res
	end

	def divisao (a,b)
		res = a/b
		puts "O resultado é #{res}"
		return res
	end

	def subtracao(a,b)
		res = a-b
		puts "O resultado é #{res}"
		return res
	end

end